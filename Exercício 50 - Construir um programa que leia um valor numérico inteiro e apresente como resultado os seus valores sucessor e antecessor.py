#Construir um programa que leia um valor numérico inteiro e apresente como resultado os seus valores: sucessor e antecessor.​
valorAtual = int(input("Digite um valor inteiro: "))
valorAntecessor = valorAtual-1
valorSucessor = valorAtual+1
print("O valor do número antecessor é", valorAntecessor)
print("O valor do número sucessor é", valorSucessor)
