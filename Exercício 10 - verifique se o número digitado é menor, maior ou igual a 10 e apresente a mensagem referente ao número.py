#Faça um algoritmo que verifique se o número digitado é menor, maior ou igual a 10 e apresente a mensagem referente ao número.

num = float(input("Digite um número: \n"))
if num > 10:
    print("O número digitado é maior do que 10: \n")
elif num < 10:
    print("O número digitado é menor do que 10: \n")
else:
    print("O número é igual a 10: \n")




